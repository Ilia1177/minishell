#include "libft.h"

void	*ft_memset(void *ptr, int x, size_t n)
{
	size_t	i;
	char	*str;

	str = ptr;
	i = 0;
	while (i < n)
	{
		str[i] = x;
		i++;
	}
	return (ptr);
}
